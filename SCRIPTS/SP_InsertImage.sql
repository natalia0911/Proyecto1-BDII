create or replace NONEDITIONABLE PROCEDURE SP_INSERTIMAGE(
       pIMAGE IN IMAGENES.IMAGEN%TYPE)
IS
BEGIN

  INSERT INTO IMAGENES 
  (
        "IMAGENID",
        "IMAGEN"

  ) 
  VALUES 
  (
        (SELECT MAX(ID) FROM SUBASTA)
       ,pIMAGE
  );

  COMMIT;
EXCEPTION
   WHEN OTHERS THEN
   ROLLBACK;

END;
